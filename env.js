  const passInput = document.getElementById("login-pass");
  const msg = document.getElementById("login-msg");
  const btn = document.getElementById("login-btn");

  // Funções do Sistema
  function validateLogin(username, password) {
    if (!username || !password) {
      return { success: false, message: "Preencha todos os campos!" };
    }

    if (usersAuth[username] && usersAuth[username] === password) {
      return { success: true, message: "Login bem-sucedido!" };
    } else {
      return { success: false, message: "Usuário ou senha incorretos!" };
    }
  }

  function saveUserData(username) {
    localStorage.setItem("loggedUser", username);
    
    const userData = usersData[username];
    if (userData) {
      localStorage.setItem("saldo_" + username, userData.saldo);
      localStorage.setItem("emprego_" + username, userData.emprego);
      localStorage.setItem("itens_" + username, JSON.stringify(userData.itens));
    }
  }

  function handleLogin() {
    const username = userInput.value.trim();
    const password = passInput.value;

    const validation = validateLogin(username, password);
    msg.textContent = validation.message;

    if (validation.success) {
      saveUserData(username);
      setTimeout(() => {
        window.location.href = "main.html";
      }, 1000);
    }
  }

  function setupEventListeners() {
    btn.addEventListener("click", handleLogin);
    
    [userInput, passInput].forEach(input => {
      input.addEventListener('keypress', (e) => {
        if (e.key === 'Enter') handleLogin();
      });
    });
  }

  // Inicialização do Sistema
  function init() {
    if (btn && userInput && passInput && msg) {
      setupEventListeners();
    } else {
      console.error("Elementos do login não encontrados!");
    }
  }

  // Inicia o sistema quando o DOM estiver pronto
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }

