// SISTEMA DE LOGIN
function initLoginSystem() {
    console.log('=== SISTEMA DE LOGIN INICIADO ===');
    
    const userInput = document.getElementById("login-user");
    const passInput = document.getElementById("login-pass");
    const msg = document.getElementById("login-msg");
    const btn = document.getElementById("login-btn");

    console.log('Elementos encontrados:', { userInput, passInput, msg, btn });

    if (!userInput || !passInput || !msg || !btn) {
        console.error('❌ Elementos de login não encontrados!');
        return;
    }

    function fazerLogin() {
        const usuario = userInput.value.trim();
        const senha = passInput.value;

        console.log('Tentando login com:', usuario);

        if (!usuario || !senha) {
            msg.textContent = "Preencha todos os campos!";
            return;
        }

        if (window.users[usuario] && window.users[usuario].senha === senha) {
            console.log('✅ LOGIN BEM-SUCEDIDO:', usuario);
            msg.textContent = "Login bem-sucedido!";
            msg.style.color = "#4CAF50";
            
            // SALVA NO LOCALSTORAGE
            localStorage.setItem("user", usuario);
            console.log('💾 Usuário salvo no localStorage:', localStorage.getItem("user"));
            
            setTimeout(() => {
                console.log('🔄 Redirecionando para main.html...');
                window.location.href = "main.html";
            }, 1000);
        } else {
            console.log('❌ LOGIN FALHOU');
            msg.textContent = "Usuário ou senha incorretos!";
            msg.style.color = "#f77";
        }
    }

    btn.addEventListener("click", fazerLogin);
    userInput.addEventListener("keypress", (e) => { 
        if (e.key === "Enter") fazerLogin(); 
    });
    passInput.addEventListener("keypress", (e) => { 
        if (e.key === "Enter") fazerLogin(); 
    });

    console.log('✅ Sistema de login configurado!');
}

// SISTEMA DO PORTAL - PARA TODAS AS PÁGINAS
function initPortalSystem() {
    console.log('=== SISTEMA DO PORTAL INICIADO ===');
    console.log('📍 Página atual:', window.location.href);
    
    // VERIFICA SE ESTÁ LOGADO
    const savedUser = localStorage.getItem("user");
    console.log('🔍 Usuário no localStorage:', savedUser);
    console.log('📋 Todo localStorage:', localStorage);
    
    // DEBUG: VERIFICA SE OS DADOS ESTÃO CARREGADOS
    console.log('🔍 Dados dos usuários carregados:', window.users);
    console.log('🔍 Usuário específico existe:', window.users && window.users[savedUser]);
    
    if (!savedUser) {
        console.error('❌ NENHUM USUÁRIO LOGADO - Redirecionando...');
        window.location.href = "index.html";
        return;
    }

    // VERIFICA SE O USUÁRIO EXISTE NOS DADOS
    if (!window.users || !window.users[savedUser]) {
        console.error('❌ USUÁRIO NÃO ENCONTRADO NOS DADOS - Redirecionando...');
        console.log('🔍 Usuário procurado:', savedUser);
        console.log('🔍 Usuários disponíveis:', Object.keys(window.users || {}));
        localStorage.removeItem('user');
        window.location.href = "index.html";
        return;
    }

    console.log('✅ Usuário logado:', savedUser);

    // ATUALIZA CABEÇALHO
    const userGreeting = document.getElementById('user-greeting');
    console.log('🔍 Elemento user-greeting:', userGreeting);
    
    if (userGreeting) {
        userGreeting.textContent = `Olá, ${savedUser}!`;
        console.log('✅ Cabeçalho atualizado para:', `Olá, ${savedUser}!`);
        
        // FORÇA A ATUALIZAÇÃO
        userGreeting.innerHTML = `Olá, ${savedUser}!`;
    } else {
        console.error('❌ Elemento user-greeting não encontrado!');
    }

    // ADICIONA BOTÃO DE SAIR
    const navbarRight = document.querySelector('.navbar-right');
    console.log('🔍 Navbar right:', navbarRight);
    
    if (navbarRight) {
        // Remove botões antigos
        const oldBtns = navbarRight.querySelectorAll('.logout-btn');
        oldBtns.forEach(btn => btn.remove());
        
        const logoutBtn = document.createElement('a');
        logoutBtn.href = "#";
        logoutBtn.className = "btn-jornal logout-btn";
        logoutBtn.textContent = "🚪 Sair";
        logoutBtn.onclick = function(e) {
            e.preventDefault();
            console.log('🚪 Saindo...');
            localStorage.removeItem('user');
            window.location.href = "index.html";
        };
        navbarRight.appendChild(logoutBtn);
        console.log('✅ Botão sair adicionado');
    }

    // ATUALIZA PÁGINA DE SALDO
    const userInfoDiv = document.getElementById('user-info');
    if (userInfoDiv && window.users[savedUser]) {
        const userData = window.users[savedUser];
        userInfoDiv.innerHTML = `
            <h3>🐸 Dados do Agente FROGS</h3>
            <p><strong>Usuário:</strong> ${savedUser}</p>
            <p><strong>Saldo Bancário:</strong> ${userData.saldo}</p>
            <p><strong>Missão Atual:</strong> ${userData.missao}</p>
            <p><strong>Status:</strong> ${userData.status}</p>
            <p><strong>Itens Adquiridos:</strong></p>
            <ul style="margin-left:20px; max-height: 300px; overflow-y: auto;">
                ${userData.itens.length > 0 ? 
                    userData.itens.map(item => `<li>${item}</li>`).join('') : 
                    '<li>Nenhum item adquirido</li>'
                }
            </ul>
        `;
        console.log('✅ Página de saldo atualizada');
    }

    console.log('✅ Sistema do portal carregado com sucesso!');
}

