// Sistema de login adaptado
const urlParams = new URLSearchParams(window.location.search);
const userFromQuery = urlParams.get('user');

if (userFromQuery) {
  localStorage.setItem('loggedUser', userFromQuery);
}

const savedUser = localStorage.getItem('loggedUser');
const userData = users[savedUser];
const username = userData ? savedUser : "Agente Desconhecido";

// Atualiza o cabeçalho
if (document.getElementById('user-greeting')) {
  document.getElementById('user-greeting').textContent = `Olá, ${username}`;
}

// Mostra dados específicos do usuário logado
if (userData && document.getElementById('user')) {
  document.getElementById('user').textContent = savedUser;
  document.getElementById('saldo').textContent = userData.saldo;
  document.getElementById('emprego').textContent = userData.emprego;
  document.getElementById('status').textContent = userData.status;
  
  // Mostra itens
  const ul = document.getElementById('itens');
  if (userData.itens.length === 0) {
    ul.innerHTML = "<li>Nenhum item adquirido.</li>";
  } else {
    userData.itens.forEach(item => {
      const li = document.createElement('li');
      li.textContent = item;
      ul.appendChild(li);
    });
  }
}

function logout() {
  localStorage.removeItem('loggedUser');
  window.location.href = "index.html";
}

// Adiciona botão de logout se necessário
if (document.querySelector('.navbar-right')) {
  const logoutBtn = document.createElement('a');
  logoutBtn.href = "#";
  logoutBtn.className = "btn-jornal";
  logoutBtn.textContent = "🚪 Sair";
  logoutBtn.onclick = logout;
  document.querySelector('.navbar-right').appendChild(logoutBtn);
}
